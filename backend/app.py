# ✅ Get instructions for a specific user
@app.route("/api/instructions", methods=["POST"])
def get_instructions():
    data = request.json
    user_id = data.get("user_id")
    phase = data.get("phase")
    day_num = data.get("day")

    if not user_id:
        return jsonify({"error": "User ID is required"}), 400

    try:
        day_num = int(day_num)
    except (ValueError, TypeError):
        return jsonify({"error": "Invalid day number"}), 400

    # Map Bengali phase to English string
    phase_map = {
        "জমি প্রস্তুতকালীন সময়কাল": "Land Preparation",
        "সংবেদনশীল সময়কাল": "Sensitive Period",
        "বৃদ্ধির সময়কাল": "Growth Period",
        "পরিপক্কতার সময়কাল": "Maturity Period"
    }

    if phase not in phase_map:
        return jsonify({"error": "Invalid phase"}), 400

    day_string = f"{phase_map[phase]} - Day {day_num}"

    # Ensure user email exists
    if not users_collection.find_one({"email": user_id}):
        return jsonify({"error": "User email does not exist."}), 404

    # Look for existing user doc in instruction collection
    user_doc = collection.find_one({"userId": user_id})

    # If new user, create default docs
    if not user_doc:
        default_docs = create_default_docs()  # this should return a list of docs with 'day', 'activities', 'extra_work'
        user_doc = {
            "userId": user_id,
            "docs": default_docs
        }
        collection.insert_one(user_doc)
    else:
        # If existing user but no 'docs' field, insert
        if "docs" not in user_doc:
            user_doc["docs"] = create_default_docs()
            collection.update_one({"userId": user_id}, {"$set": {"docs": user_doc["docs"]}})

    # Fetch updated user_doc
    user_doc = collection.find_one({"userId": user_id})

    # Find the matching day
    day_doc = next((d for d in user_doc["docs"] if d["day"] == day_string), None)

    if not day_doc:
        return jsonify({"error": f"{day_string} not found for this user"}), 404

    return jsonify(day_doc), 200

